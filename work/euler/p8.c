#define SIZE 13

int convert(const char c)
{
  return c - '0';
}

uint64_t productof(const int num[])
{
  uint64_t product = 1.0;
  for (int i = 0; i < SIZE; ++i) product *= (uint64_t)num[i];
  return product;
}

